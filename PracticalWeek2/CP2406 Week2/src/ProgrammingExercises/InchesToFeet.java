package ProgrammingExercises;

public class InchesToFeet {
    public static void main(String[] args)
    {
        int inches = 86;
        int InchToFeet = 12 ;
        System.out.println( inches + " inches corresponds to " + (inches / InchToFeet) +
                " feet and " + (inches % InchToFeet) + " inches");

    }
}
